package com.eating.pojo;

public class order {

	int order_id;
	int order_merchant;
	String order_username;
	int order_phone;
	String order_school;
	String order_dormitory;
	float order_price;
	int order_number;
	String order_state;
	
	public int getOrder_id() {
		return order_id;
	}
	public void setOrder_id(int orderId) {
		order_id = orderId;
	}
	public int getOrder_merchant() {
		return order_merchant;
	}
	public void setOrder_merchant(int orderMerchant) {
		order_merchant = orderMerchant;
	}
	public String getOrder_username() {
		return order_username;
	}
	public void setOrder_username(String orderUsername) {
		order_username = orderUsername;
	}
	public int getOrder_phone() {
		return order_phone;
	}
	public void setOrder_phone(int orderPhone) {
		order_phone = orderPhone;
	}
	public String getOrder_school() {
		return order_school;
	}
	public void setOrder_school(String orderSchool) {
		order_school = orderSchool;
	}
	public String getOrder_dormitory() {
		return order_dormitory;
	}
	public void setOrder_dormitory(String orderDormitory) {
		order_dormitory = orderDormitory;
	}
	public float getOrder_price() {
		return order_price;
	}
	public void setOrder_price(float orderPrice) {
		order_price = orderPrice;
	}
	public int getOrder_number() {
		return order_number;
	}
	public void setOrder_number(int orderNumber) {
		order_number = orderNumber;
	}
	public String getOrder_state() {
		return order_state;
	}
	public void setOrder_state(String orderState) {
		order_state = orderState;
	}
	
	
	
}
