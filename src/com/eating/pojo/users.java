package com.eating.pojo;

public class users {

	static int user_number;
	String user_name;
	String user_password;
	int user_phone;
	String user_school;
	String user_dormitory;
	
	public static int getUser_number() {
		return user_number;
	}
	public void setUser_number(int userNumber) {
		user_number = userNumber;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String userName) {
		user_name = userName;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String userPassword) {
		user_password = userPassword;
	}
	public int getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(int userPhone) {
		user_phone = userPhone;
	}
	public String getUser_school() {
		return user_school;
	}
	public void setUser_school(String userSchool) {
		user_school = userSchool;
	}
	public String getUser_dormitory() {
		return user_dormitory;
	}
	public void setUser_dormitory(String userDormitory) {
		user_dormitory = userDormitory;
	}
	
	

}
